/**
 * All of the locales supported by NewBee, represented as a string enum.
 * The key represents the country's region code, the value represents its corresponding locale string.
 */
export enum SupportedLocale {
  /**
   * United States - English
   */
  US = 'en-US',
}

/**
 * All of the possible keywords representing routes.
 */
export enum RouteKeyword {
  Home = 'Home',
  Features = 'Features',
  Guides = 'Guides',
  Pricing = 'Pricing',
  Login = 'Login',
  Register = 'Register',
  Account = 'Account',
  CreateDoc = 'CreateDoc',
  CreateQna = 'CreateQna',
  CreateOrg = 'CreateOrg',
}
